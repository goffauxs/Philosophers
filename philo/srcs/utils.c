#include "philosophers.h"

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	ft_return_error(char *msg)
{
	write(2, msg, ft_strlen(msg));
	return (0);
}
